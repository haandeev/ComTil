package com.thelab.comtil;

/**
 *
 * @author pohan
 */
public class ScriptTag {
    private String tag;
    private String value;

    public ScriptTag(String tag, String value) {
        this.tag = tag;
        this.value = value;
    }

    public String getTag() {
        return tag;
    }

    public String getValue() {
        return value;
    }
    
    
}
